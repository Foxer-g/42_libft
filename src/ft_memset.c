#include "libft.h"

void	*ft_memset(void *s, int32_t c, uint64_t n)
{
	uint64_t		i;
	unsigned char	*ptr;

	ptr = s;
	i = 0;
	while (i < n)
	{
		ptr[i] = c;
		i++;
	}
	return (s);
}
